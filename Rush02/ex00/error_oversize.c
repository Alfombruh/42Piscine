int	error_oversize (int *str)
{
	if ((str[9] >= 4) && (str[8] >= 2) && (str[7] == 9) && (str[6] >= 4)
		&& (str[6] >= 4) && (str[5] == 9) && (str[4] >= 6) && (str[3] >= 7)
		&& (str[2] >= 2) && (str[1] == 9) && (str[0] > 5))
	{
		return (0);
	}	
	return (1);
}
